    private void addUser() {
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        if (!(username.equals("") || password.equals(""))) {
            try {
                Connection con = Database.db.getConnection();
                String sql = "SELECT * FROM student.login where `username` = ?;";
                PreparedStatement st = con.prepareStatement(sql);
                st.setString(1, username);
                ResultSet rs = st.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(rootPane, "The username is Already Used ...!, Please Try Another", "Username Used", 1);
                    clear();
                } else {
                    String sql1 = "INSERT INTO `student`.`login` (`username`, `password`) VALUES (?, ?);";
                    PreparedStatement st1 = con.prepareStatement(sql1);
                    st1.setString(1, username);
                    st1.setString(2, password);
                    st1.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "The User Added Succesfully...!", "Add User", 1);
                    clear();
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Some Fields are Empty", "Emty Fields", 1);
        }
    }

    private void clear() {
        txtUsername.requestFocus();
        txtUsername.setText("");
        txtPassword.setText("");
    }

